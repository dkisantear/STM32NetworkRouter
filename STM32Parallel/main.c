/**
  * @brief  Read 4-bit parallel data from input pins
  * @retval 4-bit value (0-15)
  */
static uint8_t ReadParallelData(void)
{
    uint8_t data = 0;

    // PDATA1: PA0 (CN7 Pin 16) - Bit 0
    if (HAL_GPIO_ReadPin(PDATA1_GPIO_Port, PDATA1_Pin) == GPIO_PIN_SET)
        data |= 0x01;

    // PDATA2: PA1 (CN7 Pin 18) - Bit 1
    if (HAL_GPIO_ReadPin(PDATA2_GPIO_Port, PDATA2_Pin) == GPIO_PIN_SET)
        data |= 0x02;

    // PDATA3: PA4 (CN7 Pin 20) - Bit 2
    if (HAL_GPIO_ReadPin(PDATA3_GPIO_Port, PDATA3_Pin) == GPIO_PIN_SET)
        data |= 0x04;

    // PDATA4: PB8 (CN7 Pin 2) - Bit 3
    if (HAL_GPIO_ReadPin(PDATA4_GPIO_Port, PDATA4_Pin) == GPIO_PIN_SET)
        data |= 0x08;

    return data;
}

/**
  * @brief  Update LEDs based on 4-bit data value
  * @param  data: 4-bit value (0-15)
  */
static void UpdateLEDs(uint8_t data)
{
    // LED1 - PC6 (CN7 Pin 1) - Bit 0
    HAL_GPIO_WritePin(LED1_GPIO_Port, LED1_Pin,
                      (data & 0x01) ? GPIO_PIN_SET : GPIO_PIN_RESET);

    // LED2 - PB15 (CN7 Pin 3) - Bit 1
    HAL_GPIO_WritePin(LED2_GPIO_Port, LED2_Pin,
                      (data & 0x02) ? GPIO_PIN_SET : GPIO_PIN_RESET);

    // LED3 - PB12 (CN7 Pin 6) - Bit 2
    HAL_GPIO_WritePin(LED3_GPIO_Port, LED3_Pin,
                      (data & 0x04) ? GPIO_PIN_SET : GPIO_PIN_RESET);

    // LED4 - PB5 (CN7 Pin 13) - Bit 3
    HAL_GPIO_WritePin(LED4_GPIO_Port, LED4_Pin,
                      (data & 0x08) ? GPIO_PIN_SET : GPIO_PIN_RESET);
}

